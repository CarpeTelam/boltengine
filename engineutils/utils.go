package engineutils

import (
	"net"
	"net/http"
)

// GetIP finds the IP address from the requestor for logging purposes
func GetIP(r *http.Request) string {
	if ipProxy := r.Header.Get("X-FORWARDED-FOR"); len(ipProxy) > 0 {
		return ipProxy
	}
	ip, _, _ := net.SplitHostPort(r.RemoteAddr)
	return ip
}
